            public void actionPerformed(java.awt.event.ActionEvent evt) {
                String newUsername = updateUsernameTF.getText();
                int selectedRow = jTable1.getSelectedRow();
                if (selectedRow != -1) {
                    String oldUsername = (String) jTable1.getValueAt(selectedRow, 0);
                    Boolean status = null;
                    if (oldUsername.equals(newUsername)) {
                        JOptionPane.showMessageDialog(null, "Your inserted username is match with the old one! please try another one", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                    try {
                        status = DriverManagers.updateUsername(oldUsername, newUsername);
                    } catch (usernameAlreadyExistsException e) {
                        JOptionPane.showMessageDialog(null, e, "Error", JOptionPane.WARNING_MESSAGE);
                        return;
                    }
                    if (status) {
                        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                        model.setValueAt(newUsername, selectedRow, 0);
                        JOptionPane.showMessageDialog(null, "Username updated successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(null, "Failed to update username.", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Please select a user to update.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
