    public void searchUser() {
        SwingWorker<List<user>, String[]> worker = new SwingWorker<List<user>, String[]>() {
            @Override
            protected List<user> doInBackground() throws Exception {
                List<user> users = new ArrayList<>();
                String username = searchTF.getText().toLowerCase();
                ArrayList<user> listOfUsers = DriverManagers.searchUser(username);
                users.addAll(listOfUsers);
                for (user user : users) {
                    String[] userData = {user.getUsername(), user.getHighScore(), user.getIsActive()};
                    publish(userData);
                }
                return users;
            }

            @Override
            protected void process(List<String[]> chunks) {
                DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
                tableModel.setRowCount(0);
                if (chunks.isEmpty()) {
                    tableModel.setRowCount(0);
                } else {
                    for (String[] entry : chunks) {
                        tableModel.addRow(entry);
                    }
                }
            }

            @Override
            protected void done() {
                try {
                    List<user> users = get();
                } catch (InterruptedException | ExecutionException e) {
                    e.printStackTrace();
                }
            }
        };
        worker.execute();
    }

